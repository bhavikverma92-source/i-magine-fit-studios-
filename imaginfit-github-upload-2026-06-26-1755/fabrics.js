let fabricLibrary = [];
let activeFabricFilter = "All";

const fabricOptionsGrid = document.querySelector("[data-fabric-options]");
const fabricPhotosGrid = document.querySelector("[data-fabric-photos]");
const fabricFilters = document.querySelector("[data-fabric-filters]");
const selectedLook = new URLSearchParams(window.location.search).get("look") || "";
const builderOptionsStorageKey = "imaginfit-builder-options";
const localDesignsStorageKey = "imaginfit-local-designs";
const fabricLibraryStorageKey = "imaginfit-fabric-library";

function escapeFabricHtml(value) {
  return String(value || "")
    .replaceAll("&", "&amp;")
    .replaceAll("<", "&lt;")
    .replaceAll(">", "&gt;")
    .replaceAll('"', "&quot;")
    .replaceAll("'", "&#039;");
}

function designHref(fabric) {
  const params = new URLSearchParams();
  if (selectedLook) params.set("look", selectedLook);
  params.set("fabric", fabric);
  return `./design.html?${params.toString()}`;
}

function getFabricCategories(fabrics = fabricLibrary) {
  return ["All", ...new Set(fabrics.map((fabric) => fabric.category || "Daily Wear"))];
}

function normalizeFabricFromOption(option = {}) {
  return {
    id: `option-${String(option.label || "fabric").toLowerCase().replace(/[^a-z0-9]+/g, "-")}`,
    name: option.label || "Fabric",
    category: "Builder Option",
    bestFor: "Custom outfits from the design studio",
    comfort: "Ask studio",
    priceRange: Number(option.price || 0) ? `+₹${Number(option.price || 0).toLocaleString("en-IN")}` : "Included",
    care: "Care depends on final fabric selection",
    image: "",
    note: "Builder fabric option.",
  };
}

function removeStarterFabrics(fabrics = []) {
  return Array.isArray(fabrics) ? fabrics.filter((fabric) => !removedStarterFabricIds.has(fabric.id)) : [];
}

function mergeWithDefaultFabrics(fabrics = []) {
  const byId = new Map(defaultFabricLibrary.map((fabric) => [fabric.id, fabric]));
  removeStarterFabrics(fabrics).forEach((fabric) => byId.set(fabric.id, fabric));
  return [...byId.values()];
}

function renderFabricFilters() {
  const categories = getFabricCategories();
  fabricFilters.innerHTML = categories
    .map(
      (category) => `
        <button
          class="${category === activeFabricFilter ? "is-selected" : ""}"
          type="button"
          data-fabric-filter="${escapeFabricHtml(category)}"
        >
          ${escapeFabricHtml(category)}
        </button>
      `
    )
    .join("");
}

function renderFabrics() {
  const visibleFabrics =
    activeFabricFilter === "All"
      ? fabricLibrary
      : fabricLibrary.filter((fabric) => (fabric.category || "Daily Wear") === activeFabricFilter);

  fabricOptionsGrid.innerHTML = visibleFabrics.length
    ? visibleFabrics
        .map(
          (fabric) => `
            <article class="fabric-choice-card">
              <div class="fabric-card-media">
                ${
                  fabric.image
                    ? `<img src="${escapeFabricHtml(fabric.image)}" alt="${escapeFabricHtml(fabric.name)} fabric" />`
                    : `<span>${escapeFabricHtml(fabric.name).slice(0, 2).toUpperCase()}</span>`
                }
              </div>
              <div class="fabric-card-copy">
                <span>${escapeFabricHtml(fabric.category || "Fabric")}</span>
                <h2>${escapeFabricHtml(fabric.name)}</h2>
                <p>${escapeFabricHtml(fabric.note || "Made-to-order fabric option.")}</p>
                <dl>
                  <div><dt>Best for</dt><dd>${escapeFabricHtml(fabric.bestFor || "Custom outfits")}</dd></div>
                  <div><dt>Comfort</dt><dd>${escapeFabricHtml(fabric.comfort || "Ask studio")}</dd></div>
                  <div><dt>Range</dt><dd>${escapeFabricHtml(fabric.priceRange || "Quote after fabric selection")}</dd></div>
                  <div><dt>Care</dt><dd>${escapeFabricHtml(fabric.care || "Care shared with order")}</dd></div>
                </dl>
              </div>
              <div class="fabric-choice-bottom">
                <a class="gallery-design-link" href="${designHref(fabric.name)}">Design With ${escapeFabricHtml(
                  fabric.name
                )}</a>
              </div>
            </article>
          `
        )
        .join("")
    : '<p class="empty-cart">No fabrics in this filter yet.</p>';
}

function renderFabricPhotos(photos) {
  fabricPhotosGrid.innerHTML = photos.length
    ? photos
        .map(
          (photo) => `
            <article>
              <div class="gallery-image">
                <img src="${escapeFabricHtml(photo.image)}" alt="${escapeFabricHtml(photo.title)}" />
              </div>
              <div class="gallery-copy">
                <div class="gallery-card-top">
                  <span class="gallery-badge">Fabric</span>
                  <span>Sample</span>
                </div>
                <h3>${escapeFabricHtml(photo.title)}</h3>
                <p>${escapeFabricHtml(photo.note || "Fabric sample")}</p>
              </div>
            </article>
          `
        )
        .join("")
    : '<p class="empty-cart">No fabric photos added yet.</p>';
}

async function loadFabricLibrary() {
  try {
    const storedLibrary = localStorage.getItem(fabricLibraryStorageKey);
    if (storedLibrary !== null) {
      const localLibrary = JSON.parse(storedLibrary);
      if (Array.isArray(localLibrary)) {
        const cleanedLibrary = removeStarterFabrics(localLibrary);
        if (cleanedLibrary.length !== localLibrary.length) {
          localStorage.setItem(fabricLibraryStorageKey, JSON.stringify(cleanedLibrary));
        }
        if (cleanedLibrary.length) {
          const mergedLibrary = mergeWithDefaultFabrics(cleanedLibrary);
          if (mergedLibrary.length !== cleanedLibrary.length) {
            localStorage.setItem(fabricLibraryStorageKey, JSON.stringify(mergedLibrary));
          }
          return mergedLibrary;
        }
      }
    }
  } catch {
    // Try backend and bundled data next.
  }

  try {
    const response = await fetch("/api/fabrics");
    if (!response.ok) throw new Error("Backend unavailable");
    const fabrics = await response.json();
    if (Array.isArray(fabrics)) return mergeWithDefaultFabrics(fabrics);
  } catch {
    // Try bundled data next.
  }

  try {
    const response = await fetch("./data/fabrics.json");
    const fabrics = await response.json();
    if (Array.isArray(fabrics)) return mergeWithDefaultFabrics(fabrics);
  } catch {
    // Use empty default library.
  }

  return mergeWithDefaultFabrics(defaultFabricLibrary);
}

async function loadFabricPhotos() {
  let photos = [];

  try {
    const localDesigns = JSON.parse(localStorage.getItem(localDesignsStorageKey) || "[]");
    if (Array.isArray(localDesigns) && localDesigns.length) {
      photos = localDesigns.filter((design) => design.collection === "Fabric Samples");
      if (photos.length) return photos;
    }
  } catch {
    photos = [];
  }

  try {
    const response = await fetch("/api/designs");
    if (!response.ok) throw new Error("Backend unavailable");
    const designs = await response.json();
    photos = designs.filter((design) => design.collection === "Fabric Samples");
    if (photos.length) return photos;
  } catch {
    // Try bundled designs next.
  }

  try {
    const response = await fetch("./data/designs.json");
    const designs = await response.json();
    photos = designs.filter((design) => design.collection === "Fabric Samples");
  } catch {
    photos = [];
  }

  return photos;
}

async function loadFabricPage() {
  fabricLibrary = await loadFabricLibrary();
  renderFabricFilters();
  renderFabrics();
  renderFabricPhotos(await loadFabricPhotos());
}

fabricFilters.addEventListener("click", (event) => {
  const button = event.target.closest("[data-fabric-filter]");
  if (!button) return;
  activeFabricFilter = button.dataset.fabricFilter;
  renderFabricFilters();
  renderFabrics();
});

loadFabricPage();
